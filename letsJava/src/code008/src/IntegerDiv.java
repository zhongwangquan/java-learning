package code008.src;

public class IntegerDiv {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        System.out.println(a / b); // 3

        float c = 3.0f;
        System.out.println(a/c); // 3.3333333

        double d = 3.0;
        System.out.println(a/d); // 3.3333333333333335
    }
}
